#!/usr/bin/python3
def add(a, b):
  a = 10
  b = 5
  return(a + b)

def sub(a, b):
  a = 10
  b = 5
  return(a - b)

def mul(a, b):
  a = 10
  b = 5
  return(a * b)

def div(a, b):
  a = 10
  b = 5
  return(a / b) 

print("{} + {} =  {} ".format(a, b,add(a, b)))
print("{} - {} =  {} ".format(a, b,sub(a, b)))
print("{} * {} =  {} ".format(a, b,mul(a, b)))
print("{} / {} =  {} ".format(a, b,div(a, b)))
